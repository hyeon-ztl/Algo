SELECT C.CAR_ID, C.CAR_TYPE, ROUND (C.DAILY_FEE * 30 * ((100 -P.DISCOUNT_RATE ) / 100) , 0) AS FEE
FROM CAR_RENTAL_COMPANY_CAR AS C
JOIN CAR_RENTAL_COMPANY_DISCOUNT_PLAN AS P
ON C.CAR_TYPE = P.CAR_TYPE AND P.DURATION_TYPE LIKE ('3%')
WHERE C.CAR_TYPE IN ('세단', 'SUV') 
AND C.DAILY_FEE * 30 * ((100 -P.DISCOUNT_RATE ) / 100) < 2000000 
AND C.DAILY_FEE * 30 * ((100 -P.DISCOUNT_RATE ) / 100) >= 500000

AND CAR_ID NOT IN
(
SELECT CAR_ID
FROM CAR_RENTAL_COMPANY_RENTAL_HISTORY
WHERE    
(START_DATE >= '2022-11-01' AND START_DATE < '2022-12-01') 
OR 
(END_DATE >= '2022-11-01' AND END_DATE < '2022-12-01') 
OR 
(START_DATE < '2022-11-01' AND END_DATE > '2022-11-30') 
)

ORDER BY FEE DESC, C.CAR_TYPE ASC, C.CAR_ID DESC


-- 세단 OR SUV 
-- 11 . 1 ~ 11. 30
