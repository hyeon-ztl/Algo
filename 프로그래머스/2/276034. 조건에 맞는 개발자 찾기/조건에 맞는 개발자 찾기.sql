WITH C AS (
SELECT S.CODE
FROM SKILLCODES S
WHERE NAME = 'C#'
),

 P AS (
SELECT S.CODE
FROM SKILLCODES S
WHERE NAME = 'Python'
)

SELECT ID, EMAIL, FIRST_NAME, LAST_NAME
FROM DEVELOPERS D
CROSS JOIN C
CROSS JOIN P
WHERE (D.SKILL_CODE & C.CODE) > 0 OR
 (D.SKILL_CODE & P.CODE) > 0 
ORDER BY ID ASC;


